x = 15
y = 7
# Add two opreands 
print('x + y =', x + y)
# Substract right operand from left 
print('x - y =', x - y)
#Multiply two operands
print('x * y =', x * y)
#Divide left operand by the right one 
print('x / y =', x / y)
#Floor division - division that results into whole number adjusted to the left in th number line. 
print('x // y =', x // y)
#Modulus - reaminder of the division of operand by the right. 
print('x % y =', x % y)
#Exponent - left operand raised to the power of right   
print('x ** y =', x ** y)